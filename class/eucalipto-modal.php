                        <h5><b>Eucalipto tratado</b></h5>
                        <p class="text-justify">
                            O tratamento da madeira deve ser realizado para prevenir sua deterioração, ampliando assim seu tempo de vida
                            útil. O tratamento comumente utilizado é o químico, no qual ocorre a fixação de elementos preservativos na madeira,
                            tornando-a mais resistente à ação de fungos e insetos (brocas e cupins), principalmente se a madeira ficar em 
                            contato direto com a água ou com o solo. <br><br>
                            O tratamento da parte interna da madeira consiste na troca da seiva (madeira verde) por solução que contém elementos
                            preservantes. Após a secagem, os elementos conservantes ficarão retidos dentro da madeira. O tratamento pode ser 
                            realizado de maneira manual ou industrial (com a utilização de equipamentos específicos).<br><br>
                            O tratamento industrial é realizado a vácuo ou sob pressão em autoclave utilizando produtos preservativos 
                            regulamentados pelos órgãos competentes. Esses processos industriais são mais seguros para o meio ambiente, 
                            gerando uma contínua queda na utilização do sistema manual.<br><br>
                            A autoclave é um cilindro que suporta pressão, onde a madeira é introduzida e em seguida os produtos químicos 
                            preservantes são injetados. As pressões utilizadas são superiores a atmosférica e as etapas de tratamento são: 
                            colocação da madeira; início do vácuo; injeção da solução preservante; tratamento com o vácuo; término do vácuo
                            e retirada da solução excedente. <br><br>
                            Os preservantes de madeira podem ser compostos puros ou misturas existindo grande variação no custo, na eficiência 
                            e no modo de usar. O preservante ideal é aquele que consegue permanecer na madeira, é tóxico aos fungos e insetos, 
                            mas que não é prejudicial aos homens e animais.<br><br>
                            A autoclavagem é um moderno processo industrial de tratamento de madeira, que incorpora tecnologia desenvolvida
                            nos campos da mecânica e da química. Somente através de autoclavagem é possível impregnar profundamente a madeira 
                            com produtos inseticidas e fungicidas de ação comprovada, protegendo-a contra o apodrecimento, o cupim, e outros 
                            agentes biológicos de deterioração. Antes de ser submetida ao tratamento por autoclavagem, a madeira passa por um 
                            período de secagem natural de aproximadamente 3 meses, até apresentar um grau de umidade compatível.
                        </p><br>
                        <h5>Ciclos do Processo de Tratamento.</h5><br>
                        <p class="text-justify">
                            <label>1ª Etapa –</label> Introduz a madeira, depois de seca, no cilindro de alta pressão (autoclave) e fechar a porta. A pressão 
                            interna é igual ao da externa.<br><br>
                            <label>2ª Etapa –</label> Inicia-se o vácuo inicial, com a finalidade de extrair o ar da autoclave e das cavidades (celulares) da 
                            madeira, a 650 mmHg.<br><br>
                            <label>3ª Etapa –</label> Mantendo o vácuo, se inicia o enchimento da autoclave com a solução preservante, com a ajuda do próprio
                            vácuo existente dentro da autoclave.<br><br>
                            <label>4ª Etapa -</label> Quando a autoclave está totalmente cheia com a madeira e solução preservante, finaliza o vácuo inicial,
                            dá-se à pressão até a saturação de 18Kgf/cm².<br><br>
                            <label>5ª Etapa –</label> Finalizando a fase de pressão, a solução excedente é transferida para o tanque reservatório, evaziando-se 
                            totalmente a autoclave.<br><br>
                            <label>6ª Etapa –</label> Inicia-se o vácuo final para a retirada do excesso de solução preservativa da superfície da madeira.
                            A duração do ciclo de tratamento é de aproximadamente 4 horas.<br>
                        </p><r>
                        <h5>Secagem da madeira</h5><br>
                        <p class="text-justify">
                            A secagem da madeira visa à redução do teor de umidade que varia conforme o uso final do produto. Os objetivos
                            da secagem são: reduzir a movimentação dimensional; inibir os ataques de fungos; e aumentar a resistência física
                            da madeira.<br><br>
                            A secagem pode ser realizada ao ar livre ou em estufas com ventilação forçada (com temperatura e umidade controladas). 
                            A secagem ao ar deve ser realizada em locais abertos, empilhando as tábuas espaçadas entre si de modo a permitir que 
                            o ar circule entre as peças e diminua sua umidade. A secagem ao ar é comumente utilizada em empresas para realização da
                            pré-secagem de modo a otimizar o tempo de secagem em estufa. O ponto mais importante da secagem ao ar está na montagem 
                            da pilha de madeira que deve ser realizada com seguintes cuidados: isolamento do solo, alinhamento das peças e 
                            cobertura adequada.<br><br>
                            As vantagens da secagem em estufa são o menor tempo do processo, maior controle e obtenção de teores de umidade
                            mais baixos, porém há desvantagens como o maior custo de implantação desse sistema e de operação do equipamento.<br><br>
                            A secagem em estufa é utilizada por diversas empresas da área de movelaria, painéis, esquadrias, pisos etc. Esse
                            tipo de secagem é composto por 3 fases distintas:<br>
                        </p>
                        <ul class="text-justify">
                            <li>Aquecimento – é quando ocorre o aquecimento gradativo da temperatura em condições de elavada umidade do ar;</li>
                            <li>Secagem propriamente dita – é a etapa em que a madeira irá perder água. Nessa fase, ocorre a elevação lenta da 
                                temperatura e diminuição gradativa da umidade do ar dentro da estufa. É necessario o monitoramento para melhor 
                                controle da secagem visando a adequação ao programa previamente estabelecido, determinado pelas características
                                da madeira, pois estas influenciam na secagem;</li>
                        </ul>
                        <p class="text-justify">
                            Uniformização e condicionamento – nessa última fase, o objetivo é homogenizar a umidade dentro e entre as peças, 
                            tais como:
                        </p>
                        <ul class="text-justify">
                            <li>Fatores ligados as características da madeira: a espécie, o tipo de corte, a espessura da peça, o teor de
                                umidade inicial, a relação cerne e alburno.</li>
                            <li>Fatores do processo de secagem: a temperatura, a umidade relativa do ar e a velocidade de circulação do ar.</li>
                        </ul>
                        <p class="text-justify">
                            Espécie arbórea da família das mirtáceas, o <b>eucalipto</b> é originário da Austrália e de outras ilhas da Oceania e 
                            só <b>chegou ao Brasil</b> graças ao trabalho do agrônomo Edmundo Navarro de Andrade a partir de 1904 quando trouxe as 
                            primeiras sementes de várias espécies para atender a demanda de dormentes e madeira.<br><br>
                            O <b>Eucalipto</b> é a espécie mais usada em reflorestamento no Brasil, tendo sido plantados em <b>2001</b> uma área de três
                            milhões de hectares, quase o dobro da cultivada com pinus (um milhão e oitocentos mil hectares), a segunda espécie 
                            em área. Seu cultivo é destinado sobretudo para a produção de chapas de <b>madeira</b> e para <b>celulose</b>.<br><br>
                            Sua origem é exótica, das ilhas que compõem a <b>Oceania</b> e <b>Sudeste Asiático</b>.
                        </p><br>
                        <h5>No Brasil</h5><br>
                        <p class="text-justify">
                            Foi plantado a primeira vez no Rio Grande do Sul, no ano de 1848, mesmo ano em que mudas foram cultivadas na
                            Quinta da Boa Vista, no Rio de Janeiro. Em 1904 foi usado para a produção de lenha, combustível para a Companhia
                            Paulista de Estradas de Ferro, sendo este o primeiro registro de plantio comercial.<br><br>
                            O engenheiro Edmundo Navarro de Andrade foi o responsável pela introdução da planta para fins de reflorestamento.
                            Em 1919introduziu o plantio da espécie <i>E. urophylla</i> em <b>Rio Claro, SP</b> - tendo a mesma sido erroneamente identificada 
                            como sendo E. sexo - nativas do Timor e de Java. As mudas, entretanto, advieram do Jardim Botânico de Jacarta. Outras
                            espécies foram também cultivas em Rio Claro, o que gerou hibridações indesejáveis.<br><br>
                            Em 1937 a <b>Companhia Siderúrgica Belgo-Mineira</b> introduziu o cultivo do eucalipto como fornecedor de lenha para a
                            <b>metalurgia</b>, em sua unidade na cidade mineira de João Monlevade. Durante o <b>Regime Militar</b>, na <b>década de 1970</b>, 
                            foi implantado um modelo de grandes monoculturas do eucalipto, com grave impacto ambiental (empobrecimento e 
                            contaminação do solo, erosão, etc), além de outros problemas sociais e econômicos (decorrentes, em especial, pelos 
                            deslocamentos das populações rurais). 
                        </p>
